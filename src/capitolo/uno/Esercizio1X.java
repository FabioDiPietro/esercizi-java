package capitolo.uno;

/*
 Scrivere un programma definito dalla classe StampaContatti che stampi l’elenco di contatti di una rubrica.
 Ogni contatto dovrà essere stampato su tre righe:
 nella prima ci sarà il nome del contatto,
 nella seconda l’indirizzo,
 e nella terza il numero di telefono.
 Ogni contatto deve essere separato dal successivo da una riga vuota.
 */
public class Esercizio1X{
	public static void main(String args[]) {
			System.out.println("Elenco contatti:");
			System.out.println();
			System.out.println("Fabio Di Pietro");
			System.out.println("Via Roma 0000");
			System.out.println("11223344");
			System.out.println();
			System.out.println("Pietro Di Fabio");
			System.out.println("Via Milano 0000");
			System.out.println("33445566");
	}
}
